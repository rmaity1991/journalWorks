double summation(double * data){
    double sum=0;
    for(int i=0;i<SAMPLES;i++){
        sum+=data[i];
    }
    return sum;
}

double mean(double * data){
    double sum=summation(data);
    return sum/SAMPLES;
}

double sd(double *data){
    double mean_data=mean(data);
    double temp[SAMPLES];
    for(int i=0;i<SAMPLES;i++){
        temp[i]=pow(data[i]-mean_data,2);
    }
    return pow(summation(temp)/SAMPLES,0.5);
}

double gaussianNB(double X,double mean,double sd){
    double term1=1/sqrt((2*M_PI*pow(sd,2)));
    double term2=(-0.5)*pow(((X-mean)/sd),2);
    double result=term1*exp(term2);
    return result;
}

